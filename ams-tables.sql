-- Users Table
CREATE TABLE IF NOT EXISTS users
(
    id             BIGINT UNSIGNED AUTO_INCREMENT PRIMARY KEY, -- Auto-generated by DB (Row ID)
    user_id        VARCHAR(255) UNIQUE           NOT NULL,     -- Backend assigns user_id
    name           VARCHAR(255)                  NOT NULL,
    username       VARCHAR(255) UNIQUE DEFAULT NULL,
    email          VARCHAR(255) UNIQUE           NOT NULL,
    phone_no       VARCHAR(15) UNIQUE  DEFAULT NULL,
    gender_id      BIGINT UNSIGNED     DEFAULT NULL,           -- References the ID in generic_constants_param (Gender)
    status_id      BIGINT UNSIGNED     DEFAULT NULL,           -- References the ID in generic_constants_param (RecordStatus)
    password_hash  VARCHAR(255)                  NOT NULL,
    email_verified TINYINT(1)          DEFAULT 0,              -- 0: Not Verified, 1: Verified
    version_id     BIGINT UNSIGNED     DEFAULT 1 NOT NULL,
    created_at     TIMESTAMP           DEFAULT CURRENT_TIMESTAMP,
    updated_at     TIMESTAMP           DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    FOREIGN KEY (gender_id) REFERENCES generic_constants_param (id)
        ON DELETE SET NULL
        ON UPDATE CASCADE,
    FOREIGN KEY (status_id) REFERENCES generic_constants_param (id)
        ON DELETE SET NULL
        ON UPDATE CASCADE
);

-- Groups Table
CREATE TABLE IF NOT EXISTS ams_groups
(
    id                BIGINT UNSIGNED AUTO_INCREMENT PRIMARY KEY, -- Auto-generated by DB (Row ID)
    group_id          VARCHAR(255) UNIQUE       NOT NULL,         -- System-generated group ID (assigned by backend)
    group_name        VARCHAR(255)              NOT NULL,
    group_description TEXT,
    created_by        BIGINT UNSIGNED           NOT NULL,         -- Foreign key to users (Group Owner) (assigned by backend)
    status_id         BIGINT UNSIGNED DEFAULT NULL,               -- References the ID in generic_constants_param (RecordStatus)
    version_id        BIGINT UNSIGNED DEFAULT 1 NOT NULL,
    created_at        TIMESTAMP       DEFAULT CURRENT_TIMESTAMP,
    updated_at        TIMESTAMP       DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    FOREIGN KEY (status_id) REFERENCES generic_constants_param (id),
    FOREIGN KEY (created_by) REFERENCES users (id)                -- Backend assigns user_id
);

-- Group Members Table
CREATE TABLE IF NOT EXISTS group_members
(
    id         BIGINT UNSIGNED AUTO_INCREMENT PRIMARY KEY, -- Auto-generated by DB (Row ID)
    group_id   VARCHAR(255)              NOT NULL,         -- Backend assigns group_id
    user_id    VARCHAR(255)              NOT NULL,         -- Backend assigns user_id
    role_id    BIGINT UNSIGNED           NOT NULL,         -- References the ID in generic_constants_param (Roles)
    settings   JSON            DEFAULT NULL,               -- Stores permissions override for a specific user in the group
    status_id  BIGINT UNSIGNED DEFAULT NULL,               -- References the ID in generic_constants_param (RecordStatus)
    version_id BIGINT UNSIGNED DEFAULT 1 NOT NULL,
    created_at TIMESTAMP       DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP       DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    FOREIGN KEY (status_id) REFERENCES generic_constants_param (id),
    FOREIGN KEY (role_id) REFERENCES generic_constants_param (id),
    FOREIGN KEY (group_id) REFERENCES ams_groups (group_id),
    FOREIGN KEY (user_id) REFERENCES users (user_id),
    UNIQUE (group_id, user_id)                             -- Ensures a user can only be in a group once
);

-- Transactions Table
CREATE TABLE IF NOT EXISTS transactions
(
    id             BIGINT UNSIGNED AUTO_INCREMENT PRIMARY KEY, -- Auto-generated by DB (Row ID)
    group_id       VARCHAR(255)              NOT NULL,         -- Backend assigns group_id
    expense_amount DECIMAL(10, 2)            NOT NULL,
    purchase_item  TEXT                      NOT NULL,         -- Item purchased
    paid_by        VARCHAR(255)              NOT NULL,         -- Backend assigns user_id (the user who paid)
    version_id     BIGINT UNSIGNED DEFAULT 1 NOT NULL,
    txn_time       TIMESTAMP       DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (group_id) REFERENCES ams_groups (group_id),
    FOREIGN KEY (paid_by) REFERENCES users (user_id)
);

-- Virtual Accounts Table
CREATE TABLE IF NOT EXISTS virtual_accounts
(
    id            BIGINT UNSIGNED AUTO_INCREMENT PRIMARY KEY, -- Auto-generated by DB (Row ID)
    user_id       VARCHAR(255)              NOT NULL,         -- Backend assigns user_id
    group_id      VARCHAR(255)              NOT NULL,         -- Backend assigns group_id
    balance       DECIMAL(10, 2)  DEFAULT 0.00,               -- Balance in the group for each user
    total_balance DECIMAL(10, 2)  DEFAULT 0.00,               -- Cumulative balance across all ams_groups
    version_id    BIGINT UNSIGNED DEFAULT 1 NOT NULL,
    created_at    TIMESTAMP       DEFAULT CURRENT_TIMESTAMP,
    updated_at    TIMESTAMP       DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    FOREIGN KEY (user_id) REFERENCES users (user_id),
    FOREIGN KEY (group_id) REFERENCES ams_groups (group_id),
    UNIQUE (user_id, group_id)                                -- Ensures a single virtual account per user per group
);

-- Password Resets Table
CREATE TABLE IF NOT EXISTS password_resets
(
    id              BIGINT UNSIGNED AUTO_INCREMENT PRIMARY KEY, -- Auto-generated by DB (Row ID)
    email           VARCHAR(255)              NOT NULL,
    token           VARCHAR(255)              NOT NULL,
    version_id      BIGINT UNSIGNED DEFAULT 1 NOT NULL,
    created_at      TIMESTAMP       DEFAULT CURRENT_TIMESTAMP,
    expiration_time TIMESTAMP,                                  -- Time until token expires
    used_at         TIMESTAMP       DEFAULT NULL,               -- Time when token was used
    status_id       BIGINT UNSIGNED DEFAULT NULL,               -- References the ID in generic_constants_param (RecordStatus)
    FOREIGN KEY (status_id) REFERENCES generic_constants_param (id),
    UNIQUE (email, token)                                       -- Ensure unique token per email
);

-- Login Sessions Table
CREATE TABLE IF NOT EXISTS login_sessions
(
    id                 BIGINT UNSIGNED AUTO_INCREMENT PRIMARY KEY, -- Auto-generated by DB (Row ID)
    user_id            VARCHAR(255)              NOT NULL,         -- Backend assigns user_id
    token              VARCHAR(255)              NOT NULL,         -- Token for session management
    device_info        TEXT,                                       -- Info about the user's device
    ip_address         VARCHAR(255)    DEFAULT NULL,               -- IP address of the login
    login_time         TIMESTAMP       DEFAULT CURRENT_TIMESTAMP,
    last_activity_time TIMESTAMP       DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    status_id          BIGINT UNSIGNED DEFAULT NULL,               -- References the ID in generic_constants_param (RecordStatus)
    version_id         BIGINT UNSIGNED DEFAULT 1 NOT NULL,
    expiry_time TIMESTAMP DEFAULT NULL,
    FOREIGN KEY (user_id) REFERENCES users (user_id),
    FOREIGN KEY (status_id) REFERENCES generic_constants_param (id),
    UNIQUE (user_id)                                               -- Ensures only one active session per user
);

-- Activity Logs Table
CREATE TABLE IF NOT EXISTS activity_logs
(
    id         BIGINT UNSIGNED AUTO_INCREMENT PRIMARY KEY, -- Auto-generated by DB (Row ID)
    user_id    VARCHAR(255)              NOT NULL,         -- Backend assigns user_id
    action     VARCHAR(255)              NOT NULL,
    details    TEXT,
    version_id BIGINT UNSIGNED DEFAULT 1 NOT NULL,
    created_at TIMESTAMP       DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (user_id) REFERENCES users (user_id)
);

-- User Roles Table
CREATE TABLE IF NOT EXISTS user_roles
(
    id         BIGINT UNSIGNED AUTO_INCREMENT PRIMARY KEY, -- Auto-generated by DB (Row ID)
    user_id    VARCHAR(255)              NOT NULL,         -- Backend assigns user_id
    group_id   VARCHAR(255)              NOT NULL,         -- Backend assigns group_id
    role_id    BIGINT UNSIGNED           NOT NULL,         -- References the ID in generic_constants_param (Roles)
    version_id BIGINT UNSIGNED DEFAULT 1 NOT NULL,
    FOREIGN KEY (user_id) REFERENCES users (user_id),
    FOREIGN KEY (group_id) REFERENCES ams_groups (group_id),
    FOREIGN KEY (role_id) REFERENCES generic_constants_param (id)
);



CREATE TABLE IF NOT EXISTS role_permissions
(
    role_id       BIGINT UNSIGNED NOT NULL, -- Foreign key to roles (generic_constants_param with dtype = 'Role')
    permission_id BIGINT UNSIGNED NOT NULL, -- Foreign key to permissions (generic_constants_param with dtype = 'Permission')
    PRIMARY KEY (role_id, permission_id),   -- Composite primary key for the role-permission pair
    FOREIGN KEY (role_id) REFERENCES generic_constants_param (id)
        ON DELETE CASCADE,                  -- Reference to roles in generic_constants_param
    FOREIGN KEY (permission_id) REFERENCES generic_constants_param (id)
        ON DELETE CASCADE                   -- Reference to permissions in generic_constants_param
);

CREATE TABLE otp_verification
(
    id          INT AUTO_INCREMENT PRIMARY KEY,
    email       VARCHAR(255) NOT NULL,
    otp_code    VARCHAR(6)   NOT NULL,
    created_at  TIMESTAMP  DEFAULT CURRENT_TIMESTAMP,
    expired_at  TIMESTAMP    NOT NULL,
    is_verified TINYINT(1) DEFAULT 0,
    UNIQUE (email, otp_code)
);


