-- Generic Constants Parameter Table
CREATE TABLE IF NOT EXISTS generic_constants_param
(
    id            BIGINT UNSIGNED AUTO_INCREMENT PRIMARY KEY,                            -- Auto-generated by DB
    dtype         VARCHAR(255)              NOT NULL,                                    -- Type of constant (Roles, Gender, etc.)
    code          VARCHAR(255)              NOT NULL,                                    -- The code for the constant value (e.g., SuperAdmin, Admin)
    description   TEXT,                                                                  -- Description of the constant value
    version_id    BIGINT UNSIGNED DEFAULT 1 NOT NULL,                                    -- Version ID for tracking changes
    active_status TINYINT(1)      DEFAULT 1 NOT NULL,                                    -- Active status (1: Active, 0: Inactive)
    created_at    TIMESTAMP       DEFAULT CURRENT_TIMESTAMP,                             -- Insertion date
    updated_at    TIMESTAMP       DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP, -- Modification date
    UNIQUE (dtype, code, active_status)                                                  -- Ensure dtype, code, and active_status combination is unique
);


